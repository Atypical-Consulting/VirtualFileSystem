    /// <summary>
    /// Get the SVG icon path for a file extension
    /// </summary>
    public string GetIconForExtension(string? extension)
    {
        if (string.IsNullOrEmpty(extension))
            return DocumentIcon;

        return extension.ToLowerInvariant() switch
        {
            // Documents
            ".txt" or ".md" or ".markdown" => DocumentTextIcon,
            ".pdf" => DocumentIcon,
            ".doc" or ".docx" => DocumentIcon,
            ".xls" or ".xlsx" => TableCellsIcon,
            ".ppt" or ".pptx" => PresentationIcon,

            // Code files
            ".cs" or ".csx" => CodeBracketIcon,
            ".js" or ".jsx" or ".ts" or ".tsx" => CodeBracketIcon,
            ".html" or ".htm" or ".razor" => CodeBracketIcon,
            ".css" or ".scss" or ".sass" or ".less" => CodeBracketIcon,
            ".json" or ".xml" or ".yaml" or ".yml" => CodeBracketIcon,
            ".py" or ".rb" or ".go" or ".rs" or ".java" => CodeBracketIcon,
            ".sql" => CircleStackIcon,

            // Images
            ".jpg" or ".jpeg" or ".png" or ".gif" or ".bmp" or ".svg" or ".webp" => PhotoIcon,

            // Videos
            ".mp4" or ".mov" or ".avi" or ".mkv" or ".webm" => FilmIcon,

            // Audio
            ".mp3" or ".wav" or ".ogg" or ".flac" or ".aac" => MusicalNoteIcon,

            // Archives
            ".zip" or ".rar" or ".7z" or ".tar" or ".gz" => ArchiveBoxIcon,

            // Config files
            ".env" or ".config" or ".ini" or ".toml" => CogIcon,
            ".gitignore" or ".editorconfig" or ".eslintrc" => CogIcon,

            _ => DocumentIcon
        };
    }

    /// <summary>
    /// Get the color class for a file extension
    /// </summary>
    public string GetColorForExtension(string? extension)
    {
        if (string.IsNullOrEmpty(extension))
            return "text-gray-500";

        return extension.ToLowerInvariant() switch
        {
            // Documents
            ".txt" or ".md" => "text-gray-600",
            ".pdf" => "text-red-500",
            ".doc" or ".docx" => "text-blue-600",
            ".xls" or ".xlsx" => "text-green-600",
            ".ppt" or ".pptx" => "text-orange-500",

            // Code files
            ".cs" or ".csx" => "text-purple-600",
            ".js" or ".jsx" => "text-yellow-500",
            ".ts" or ".tsx" => "text-blue-500",
            ".html" or ".htm" or ".razor" => "text-orange-600",
            ".css" or ".scss" => "text-pink-500",
            ".json" => "text-amber-600",
            ".py" => "text-blue-500",
            ".sql" => "text-cyan-600",

            // Images
            ".jpg" or ".jpeg" or ".png" or ".gif" or ".svg" => "text-emerald-500",

            // Videos
            ".mp4" or ".mov" or ".avi" => "text-purple-500",

            // Audio
            ".mp3" or ".wav" => "text-pink-600",

            // Archives
            ".zip" or ".rar" or ".7z" => "text-amber-700",

            // Config
            ".env" or ".config" => "text-slate-600",

            _ => "text-gray-500"
        };
    }

